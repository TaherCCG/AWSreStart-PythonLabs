"""
Numeric Data Types
"""

print("Python has three numeric types: int, float, and complex")

# int data type
myValue=1                                                           #sets myValue as a variable with data, it will remain as 1 unles
print(myValue)
print(type(myValue))
print(str(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

# float data type
myValue=3.14
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

# complex data type
myValue=5j
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

# bool data type
myValue=True
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue=False
print(myValue)
print(type(myValue))
print(str(myValue) + " is of the data type " + str(type(myValue)))

